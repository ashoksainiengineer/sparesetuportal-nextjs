// 7. Generate RCCBs / ELCBs
function generateRCCB(make) {
    const poles = ["2P (1Ph+N)", "4P (3Ph+N)"];
    const amps = ["16A", "25A", "32A", "40A", "63A", "80A", "100A"];
    const sensitivity = ["30mA", "100mA", "300mA"];
    const types = [
        { code: "Type AC", usage: "Standard AC Loads" },
        { code: "Type A", usage: "Pulsating DC/Electronics" }
    ];
    
    let results = [];
    poles.forEach(p => {
        amps.forEach(a => {
            sensitivity.forEach(s => {
                types.forEach(t => {
                    results.push({
                        cat: "Switchgear",
                        sub: "Residual Current Circuit Breaker (RCCB/ELCB)",
                        make: make,
                        model: `${t.code} (${t.usage})`,
                        spec: `${p} - ${a} - ${s}`
                    });
                });
            });
        });
    });
    return results;
}
