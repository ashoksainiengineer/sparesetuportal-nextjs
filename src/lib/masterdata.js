// 3. Generate LT Power Cables (1.1 kV)
function generateLTCables(code, type, mat, volt) {
    const sizes = ["4", "6", "10", "16", "25", "35", "50", "70", "95", "120", "150", "185", "240", "300", "400"];
    const cores = ["3C", "3.5C", "4C"];
    let results = [];
    
    cores.forEach(core => {
        sizes.forEach(size => {
            // For 3.5C, skip small sizes (technically don't exist in power cables)
            if (core === "3.5C" && parseInt(size) < 25) return;

            results.push({
                cat: "Cables",
                sub: `LT ${mat === 'Al' ? 'Aluminium' : 'Copper'} Power Cable`,
                make: "Standard",
                model: `${code} (${mat === 'Al' ? 'Aluminium' : 'Copper'})`,
                spec: `${core} x ${size} sqmm ${mat} ${type} (${volt})`
            });
        });
    });
    return results;
}

